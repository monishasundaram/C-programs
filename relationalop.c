#include<stdio.h>
int main(){
  int a = 5;
  int b = 7;
  //Equalto operator
  printf("a == b : %d\n", a == b);
  //Notequalto operator
  printf("a != b : %d\n", a != b);
  //Greater than operator
  printf("a > b  : %d\n", a > b);
  //Less than operator
  printf("a < b  : %d\n", a < b);
  //Greater than equal to operator
  printf("a >= b : %d\n", a >= b);
  //Less than equal to operator
  printf("a <= b : %d\n", a <= b);

  return 0;
}
